package com.example.fitmyjob;

public class Jobitem {

    String company_name, date, description, location, salary, contact_no;

    public String getCompany_name() {
        return company_name;
    }
    public void setCompany_name(String company_name) { company_name = company_name; }

    public String getDate() {
        return date;
    }
    public void setDate(String date) { date = date; }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) { description = description; }

    public String getLocation() {
        return location;
    }
    public void setLocation(String location) { location = location; }

    public String getSalary() {
        return salary;
    }
    public void setSalary(String salary) {salary = salary; }

    public String getContact_no() {
        return contact_no;
    }
    public void setContact_no(String contact_no) {contact_no = contact_no; }
}
